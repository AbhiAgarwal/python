# Chaning Comparisons are comparisons of more than 2 numbers
print(11 < 15 < 20) # True
print(20 < 19 < 29) # False

# Trying a numerical example
comparison = 10
print(400 > 100 > comparison) # True
print(10 < comparison * 9 < 100 < comparison * 100) # True

